package com.msu.io.pojo;

public class PlainObjectItem {
	
	public int city;
	public double weight;
	public double profit;
	
	
	
	public PlainObjectItem() {
		super();
	}



	public PlainObjectItem(int city, double weight, double profit) {
		super();
		this.city = city;
		this.weight = weight;
		this.profit = profit;
	}
	
	
	

}
