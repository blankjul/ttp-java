package com.moo.ttp.model;


public class Item {
	
	
	private int weight;
	
	private int profit;

	
	public Item(int profit, int weight) {
		super();
		this.weight = weight;
		this.profit = profit;
	}

	public int getWeight() {
		return weight;
	}

	public int getProfit() {
		return profit;
	}
	
	
	
		

}
